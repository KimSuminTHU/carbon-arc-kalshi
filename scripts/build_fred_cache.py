def write(name: str, indicator: str, series_id: str, data: list[tuple[str, float]]) -> None:
    obj = {
        "indicator": indicator,
        "series_id": series_id,
        "frequency": "monthly",
        "data": [{"date": d, "value": v} for d, v in data],
    }
    (OUT / name).write_text(json.dumps(obj))
    print(f"wrote {OUT / name}: {len(data)} rows")


